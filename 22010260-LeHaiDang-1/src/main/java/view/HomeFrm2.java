    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    String tenVuKhi = lblTenVuKhi.getText();
    String hanhDong = cbxHanhDong.getSelectedItem().toString();
    int soLuong = 0, soLuongDan = 0;
    Date ngay = jDateChooser1.getDate();
    boolean isOK = true;

    if (tenVuKhi == null || tenVuKhi.isEmpty()) {
        JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn Tên Vũ Khí!");
        isOK = false;
    }

    if (ngay == null) {
        JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn Ngày!");
        isOK = false;
    }

    try {
        soLuong = Integer.parseInt(txtSoLuong.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(rootPane, "Số Lượng không hợp lệ!");
        isOK = false;
    }

    try {
        soLuongDan = Integer.parseInt(txtSoLuongDan.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(rootPane, "Số Lượng Đạn không hợp lệ!");
        isOK = false;
    }

    if (isOK) {
        ThuXuat thuXuat = new ThuXuat(tenVuKhi, hanhDong, ngay, soLuong, soLuongDan);
        addThuXuat(thuXuat); // Thêm thuXuat vào danh sách thuXuats
        JOptionPane.showMessageDialog(rootPane, "Đã Thực Hiện!");
    }
        showData();
        
    }//GEN-LAST:event_btnAddActionPerformed

