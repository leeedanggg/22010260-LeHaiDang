    private void loadDataToTable(){
        thuXuats = dao.getListThuXuat();
        tblModel2.setRowCount(0);
        for (ThuXuat thuXuat : thuXuats) {tblModel2.addRow(new Object[]{thuXuat.getTenVuKhi(), thuXuat.getNgay(), thuXuat.getHanhDong(), thuXuat.getSoLuong(), thuXuat.getSoLuongDan()});
        }
    }
    public void showData(){
        tblModel2.setRowCount(0);
        for (ThuXuat thuXuat : thuXuats) {
            tblModel2.addRow(new Object[]{thuXuat.getTenVuKhi(), thuXuat.getNgay(), thuXuat.getHanhDong(), thuXuat.getSoLuong(), thuXuat.getSoLuongDan()});
        }
        
    }
    public void addThuXuat(ThuXuat t){
        thuXuats.add(t);
        showData();
    }
    private void saveThuXuats() {
        try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("thuXuats.dat"))) {
            oos.writeObject(thuXuats);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    private void loadThuXuats() {
        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream("thuXuats.dat"))) {
            thuXuats = (List<ThuXuat>) ois.readObject();
            for (ThuXuat thuXuat : thuXuats) {
                tblModel2.addRow(new Object[]{thuXuat.getTenVuKhi(), thuXuat.getNgay(), thuXuat.getHanhDong(), thuXuat.getSoLuong(), thuXuat.getSoLuongDan()});
            }
        } catch (IOException | ClassNotFoundException e) {
            e.printStackTrace();
        }
    }

    
    
    

