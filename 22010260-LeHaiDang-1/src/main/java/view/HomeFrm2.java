    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        int removeIndex = tblThuXuat.getSelectedRow();
        if(removeIndex == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn vũ khí cần xóa!");
        }else if(thuXuats.size() == 0 ){
        JOptionPane.showMessageDialog(rootPane, "Không có vũ khí để xóa!");
}   else{
            thuXuats.remove(removeIndex);
            showData();
        }  
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        selectedIndex = tblThuXuat.getSelectedRow();
        if(thuXuats.size() == 0 ){
            JOptionPane.showMessageDialog(rootPane, "Hãy thêm hành động!");
            
        }else if(selectedIndex == -1  ){
             JOptionPane.showMessageDialog(rootPane, "Hãy chọn phần cần sửa!");
        }
        else{
            EditFrm2 edit = new EditFrm2(this, rootPaneCheckingEnabled);
            edit.setEditData(thuXuats.get(selectedIndex));
            edit.setVisible(true);
        }
        
        
    }//GEN-LAST:event_btnEditActionPerformed

